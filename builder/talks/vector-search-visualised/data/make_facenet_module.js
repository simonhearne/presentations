#!/usr/bin/env node
// Regenerates the FACES data block in ../facenet-learning.js.
//
// The face JPEGs are inlined as base64 data URIs because the build
// base64-encodes the whole module, so a relative ./data/faces/... path
// would not resolve at runtime. Only the region between the FACES_START
// and FACES_END sentinels is rewritten; the rest of the module is
// hand-written and left untouched.

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const facesDir = join(here, 'faces');
const modulePath = resolve(here, '..', 'facenet-learning.js');

// Curated subset: one face per identity, paired with an identity colour.
// Length and order MUST match CLUSTERS in facenet-learning.js.
const IDENTITIES = [
  { file: '003983.jpg', color: '#5b86e8' },
  { file: '016605.jpg', color: '#e8a14b' },
  { file: '020172.jpg', color: '#6dd6a0' },
  { file: '042926.jpg', color: '#e87da8' },
  { file: '063886.jpg', color: '#a98de8' },
];

const START =
  '/* FACES_START — generated by data/make_facenet_module.js, do not edit by hand */';
const END = '/* FACES_END */';

const entries = IDENTITIES.map(({ file, color }) => {
  const b64 = readFileSync(join(facesDir, file)).toString('base64');
  return `  { color: ${JSON.stringify(color)}, dataUri: "data:image/jpeg;base64,${b64}" }`;
});
const block = `${START}\nconst FACES = [\n${entries.join(',\n')}\n];\n${END}`;

const src = readFileSync(modulePath, 'utf8');
const startIdx = src.indexOf(START);
const endIdx = src.indexOf(END);
if (startIdx === -1 || endIdx === -1) {
  throw new Error('FACES sentinels not found in facenet-learning.js');
}
if (endIdx < startIdx) {
  throw new Error('FACES_END appears before FACES_START in facenet-learning.js');
}
const next = src.slice(0, startIdx) + block + src.slice(endIdx + END.length);
writeFileSync(modulePath, next);
console.log(`wrote FACES block to ${modulePath} (${IDENTITIES.length} faces)`);
